"use strict";

function _classCallCheck(instance, Constructor) { if (!(instance instanceof Constructor)) { throw new TypeError("Cannot call a class as a function"); } }

var Icon = function Icon() {
    _classCallCheck(this, Icon);

    this.mimetype = null;
    this.width = null;
    this.height = null;
    this.depth = null;
    this.url = null;
};

module.exports = Icon;